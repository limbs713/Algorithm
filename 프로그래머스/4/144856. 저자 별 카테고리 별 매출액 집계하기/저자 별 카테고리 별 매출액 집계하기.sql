SELECT A.AUTHOR_ID, A.AUTHOR_NAME, S.CATEGORY, SUM(S.SALES) AS TOTAL_SALES
FROM (
    SELECT B.BOOK_ID, B.CATEGORY, B.AUTHOR_ID , SUM(S.SALES) * B.PRICE AS SALES
    FROM BOOK_SALES S JOIN BOOK B ON S.BOOK_ID = B.BOOK_ID
    WHERE S.SALES_DATE BETWEEN DATE(20220101) AND DATE(20220131)
    GROUP BY S.BOOK_ID
)AS S JOIN AUTHOR A ON S.AUTHOR_ID = A.AUTHOR_ID
GROUP BY A.AUTHOR_ID, S.CATEGORY
ORDER BY A.AUTHOR_ID, S.CATEGORY DESC